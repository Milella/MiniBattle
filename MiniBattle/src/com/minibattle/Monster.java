package com.minibattle;


public class Monster {

	private String monsterDescription = "ORC";
	private int monsterHP = 100;
	private int monsterAC = 10;
	private int monsterDamage = 20;
	
	
	public String getMonsterDescription() {
		return monsterDescription;
	}
	public void setMonsterDescription(String monsterDescription) {
		this.monsterDescription = monsterDescription;
	}
	public int getMonsterHP() {
		return monsterHP;
	}
	public void setMonsterHP(int monsterHP) {
		this.monsterHP = monsterHP;
	}
	public int getMonsterAC() {
		return monsterAC;
	}
	public void setMonsterAC(int monsterAC) {
		this.monsterAC = monsterAC;
	}
	public int getMonsterDamage() {
		return monsterDamage;
	}
	public void setMonsterDamage(int monsterDamage) {
		this.monsterDamage = monsterDamage;
	}
	
	
	
	
}
