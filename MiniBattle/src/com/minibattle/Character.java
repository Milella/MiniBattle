package com.minibattle;

public class Character {

	private String characterName = "Default Character";
	private int characterHP = 100;
	private int characterAC = 10;
	private int characterDamange = 20;
	
	
	
	public String getCharacterName() {
		return characterName;
	}
	public void setCharacterName(String characterName) {
		this.characterName = characterName;
	}
	public int getCharacterHP() {
		return characterHP;
	}
	public void setCharacterHP(int characterHP) {
		this.characterHP = characterHP;
	}
	public int getCharacterAC() {
		return characterAC;
	}
	public void setCharacterAC(int characterAC) {
		this.characterAC = characterAC;
	}
	public int getCharacterDamange() {
		return characterDamange;
	}
	public void setCharacterDamange(int characterDamange) {
		this.characterDamange = characterDamange;
	}
	
	
	
	
}
